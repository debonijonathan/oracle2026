# Funzione oracolo finale
def oracolo(squadra_casa, squadra_trasferta, neutro=True):
    r_casa, pts_casa = get_rank_at_date(squadra_casa, pd.Timestamp.now())
    r_trasf, pts_trasf = get_rank_at_date(squadra_trasferta, pd.Timestamp.now())

    p_casa, gf_casa, gs_casa, _ = forma_squadra(squadra_casa, pd.Timestamp.now())
    p_trasf, gf_trasf, gs_trasf, _ = forma_squadra(squadra_trasferta, pd.Timestamp.now())

    h2h_h, h2h_par, h2h_a = head_to_head(squadra_casa, squadra_trasferta, pd.Timestamp.now())

    match = pd.DataFrame([{
        'diff_rank': r_casa - r_trasf,
        'diff_pts': pts_casa - pts_trasf,
        'forma_punti_home': p_casa,
        'forma_punti_away': p_trasf,
        'diff_forma': p_casa - p_trasf,
        'gol_fatti_home': gf_casa,
        'gol_fatti_away': gf_trasf,
        'gol_subiti_home': gs_casa,
        'gol_subiti_away': gs_trasf,
        'h2h_home': h2h_h,
        'h2h_away': h2h_a,
        'h2h_par': h2h_par,
        'neutral': int(neutro),
        'tournament_weight': 5,  # Mondiali
        'rank_home': r_casa,
        'rank_away': r_trasf,
    }])

    proba = modello_finale.predict_proba(match)[0]
    pred = predici_con_soglia([proba])[0]
    nomi = {0: f'Vittoria {squadra_casa}', 1: 'Pareggio', 2: f'Vittoria {squadra_trasferta}'}

