    print(f"  Pareggio:      {proba[1]:.1%}")
    print(f"  {squadra_trasferta}: {proba[2]:.1%}")
    print(f"  → Esito: {nomi[pred]}")

    return {'home': squadra_casa, 'away': squadra_trasferta,
            'prob_home': round(proba[0], 3), 'prob_draw': round(proba[1], 3),
            'prob_away': round(proba[2], 3), 'prediction': pred}

