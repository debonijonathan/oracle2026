SOGLIA_PAREGGIO = 0.27

def predici_con_soglia(proba, soglia_par=SOGLIA_PAREGGIO):
    risultati = []
    for p in proba:
        p_casa, p_par, p_trasf = p[0], p[1], p[2]
        if p_par >= soglia_par:
            risultati.append(1)
        else:
            risultati.append(np.argmax([p_casa, p_par, p_trasf]))
    return np.array(risultati)

