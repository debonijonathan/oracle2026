# ── 2. RANKING FIFA STORICO (senza data leakage) ─────────────────
# Per ogni partita usiamo il rank più recente PRIMA della data
ranking_sorted = ranking.sort_values('rank_date')

def get_rank_at_date(team, date):
    mask = (ranking_sorted['country_full'] == team) & (ranking_sorted['rank_date'] <= date)
    rows = ranking_sorted[mask]
    if not rows.empty:
        return rows.iloc[-1]['rank'], rows.iloc[-1]['total_points']
    return 100, 500  # default se non trovato

# ── 3. FORMA RECENTE ─────────────────────────────────────────────
# Punti, gol fatti, gol subiti negli ultimi N match pesati per importanza torneo
def forma_squadra(team, date, n=10):
    partite = df[
        ((df['home_team'] == team) | (df['away_team'] == team)) &
        (df['date'] < date)
    ].tail(n)

    if partite.empty:
        return 0, 0, 0, 0  # punti, gol_fatti, gol_subiti, partite_giocate

    punti_tot = 0
    gol_fatti = 0
    gol_subiti = 0
    peso_tot = 0

    for _, r in partite.iterrows():
        peso = r['tournament_weight']
        if r['home_team'] == team:
            gf, gs = r['home_score'], r['away_score']
        else:
            gf, gs = r['away_score'], r['home_score']

        gol_fatti += gf * peso
        gol_subiti += gs * peso
        peso_tot += peso

        if gf > gs:
            punti_tot += 3 * peso
        elif gf == gs:
            punti_tot += 1 * peso

    n_partite = len(partite)
    return (
        punti_tot / peso_tot if peso_tot > 0 else 0,
        gol_fatti / peso_tot if peso_tot > 0 else 0,
        gol_subiti / peso_tot if peso_tot > 0 else 0,
        n_partite
    )

# ── 4. HEAD TO HEAD ──────────────────────────────────────────────
def head_to_head(team_a, team_b, date, n=10):
    scontri = df[
        (
            ((df['home_team'] == team_a) & (df['away_team'] == team_b)) |
            ((df['home_team'] == team_b) & (df['away_team'] == team_a))
        ) &
        (df['date'] < date)
    ].tail(n)

    if scontri.empty:
        return 0, 0, 0  # vittorie_a, pareggi, vittorie_b

    v_a, par, v_b = 0, 0, 0
    for _, r in scontri.iterrows():
        if r['home_team'] == team_a:
            hs, as_ = r['home_score'], r['away_score']
        else:
            hs, as_ = r['away_score'], r['home_score']

        if hs > as_: v_a += 1
        elif hs == as_: par += 1
        else: v_b += 1

    tot = len(scontri)
    return v_a/tot, par/tot, v_b/tot

