# ── SIMULAZIONE VELOCE ──────────────────────────────────────────
def simula_ko(team_a, team_b):
    """Vincitore K.O. — nessuna chiamata al modello, usa cache"""
    key = (team_a, team_b)
    if key in prob_cache:
        p = prob_cache[key]
    else:
        p = [0.33, 0.33, 0.34]
    r = random.random()
    if r < p[0]: return team_a
    elif r < p[0] + p[1]: return team_a if random.random() < 0.5 else team_b
    else: return team_b

def simula_girone_fast(squadre):
    """Simula girone usando prob_cache, restituisce classifica"""
    cls = {s: {'pt':0,'gf':0,'gs':0} for s in squadre}

    for home, away in itertools.combinations(squadre, 2):
        # Usa risultato reale se disponibile
        reale = risultati_reali.get((home, away)) or risultati_reali.get((away, home))
        if reale and reale['home_score'] is not None:
            inv = risultati_reali.get((away, home))
            hs = reale['away_score'] if inv else reale['home_score']
            as_ = reale['home_score'] if inv else reale['away_score']
        else:
            # Simula stocasticamente
            key = (home, away)
            p = prob_cache.get(key, [0.33, 0.33, 0.34])
            r = random.random()
            if r < p[0]: hs, as_ = 2, 0
            elif r < p[0]+p[1]: hs, as_ = 1, 1
            else: hs, as_ = 0, 2

        cls[home]['gf'] += hs; cls[home]['gs'] += as_
        cls[away]['gf'] += as_; cls[away]['gs'] += hs
        if hs > as_: cls[home]['pt'] += 3
        elif hs == as_: cls[home]['pt'] += 1; cls[away]['pt'] += 1
        else: cls[away]['pt'] += 3

    return sorted(cls.items(), key=lambda x: (-x[1]['pt'], -(x[1]['gf']-x[1]['gs']), -x[1]['gf']))

def simula_torneo_fast():
    # 1. Gironi
    primi, secondi, terze_list = [], [], []
    for g, squadre in GIRONI.items():
        cls = simula_girone_fast(squadre)
        primi.append(cls[0][0])
        secondi.append(cls[1][0])
        terze_list.append((cls[2][0], cls[2][1]['pt']))

    # 2. Migliori 8 terze
    terze_ok = [t for t,_ in sorted(terze_list, key=lambda x: -x[1])[:8]]

    # 3. Round of 32: 12 primi vs 12 terze + 12 secondi vs altro secondo (semplificato)
    r32 = []
    for i in range(12):
        r32.append((primi[i], terze_ok[i % 8]))
    for i in range(0, 12, 2):
        r32.append((secondi[i], secondi[i+1]))

    # Deduplica e prendi 16
    seen, clean = set(), []
    for a, b in r32:
        k = frozenset([a,b])
        if k not in seen and a != b:
            seen.add(k); clean.append((a,b))
        if len(clean) == 16: break

    # 4. Fasi K.O.
    def fase(partite):
        v = [simula_ko(a,b) for a,b in partite]
        return v, [(v[i],v[i+1]) for i in range(0,len(v)-1,2)]

    v32, q = fase(clean)
    v8, s = fase(q)
    v4, f = fase(s)
    return simula_ko(f[0][0], f[0][1]) if f else v4[0]

